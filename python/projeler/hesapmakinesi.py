num1=int(input('1. sayıyı giriniz  '))
num2=int(input('2. sayıyı giriniz  '))
op=input('işlemi seçiniz(+,-,*,/)  ')
if op=='*':
    print(num1, '*', num2,'=', num1*num2)
elif op=='+':
 print(num1, '+', num2,'=', num1+num2)
elif op == '-':
 print(num1, '-', num2, '=', num1-num2)
elif op=='/':
 print(num1,'/',num2,'=', num1/num2)
